package study.class_.inheritance;

public class Test01 {
    // 필드
    private int a;
    private int b;

    // 생성자
    public Test01(){ }

    public Test01(int a, int b){
        this.a = a;
        this.b = b;

    }
    // 메서드
    // Getter/Setter

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
    // 정적

    // 동적
}
