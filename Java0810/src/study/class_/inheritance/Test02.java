package study.class_.inheritance;

public class Test02 {
    // 필드
    private int c;
    private int d;

    // 생성자
    public Test02(){ }
    public Test02(int c, int d){
        this.c = c;
        this.d = d;

    }
    // 메서드
    // Getter/Setter

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }
    // 정적

    // 동적
}
